package replit;

public class ForWhile_8_adanzyeAsii {

	public static void main(String[] args) {
		
		/*
		 'a' ile 'z' arasýndaki karakterlerin ascii deðerlerini 
		 	yazdýran bir Java programý yazýn.
		 */
		char ch='a';
				
		while (ch<='z') {
		
			System.out.println("Ascii value of "+ch+" = "+(ch+0));
			
			ch++;
		}
		
	}

}
